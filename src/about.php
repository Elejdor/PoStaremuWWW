    <h2 class="mt-4">O NAS</h2>
    <p>
    Badamy dziedzictwo naszych przodków, odświeżamy je i włączamy z powrotem w obieg. Interesują nas rzeczy cenne i prawdziwe,
    które zachowały się w naszej kulturze. Ciekawi nas wszystko to, co wciąż jest wartościowe i może przynosić korzyści społeczeństwu,
    naszym bliskim i nam samym.
    </p>
    <h2 class="mt-4">MISJA</h2>
    <p>Misją Fundacji Po Staremu są poszukiwania cennych i wartościowych elementów spuścizny kulturowej po naszych przodkach (zwyczaje, praktyki, umiejętności i wiedza), przejmowanie ich poprzez naukę i praktykę, a następnie upowszechnianie za pomocą warsztatów
    i wydarzeń kulturalnych. W ten sposób liczymy na ponowne włączenie w obieg najcenniejszych elementów.</p>
