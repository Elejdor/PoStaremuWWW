<h2>Mistrz Sławomir Czekalski</h2>

<img class ="col-sm-4" src="../img/czekalski-683x1024.jpg" align="left"/>

<p>Fundacja Po Staremu z Łodzi oraz Żychliński Dom Kultury zapraszają do udziału w projekcie „Mistrz Sławomir Czekalski. 
    Popularyzacja i kontynuacja muzycznych tradycji pogranicza łęczycko-łowickiego”. Projekt dofinansowany ze środków Ministra Kultury i Dziedzictwa Narodowego.</p>

<p>Sławomir Czekalski to wielokrotnie wyróżniany na przeglądach i w konkursach harmonista z pogranicza łęczycko-łowickiego. 
    Jego kapela Dobrzeliniacy doceniana jest również na scenach tanecznych w Łodzi, Warszawie, a nawet w Trójmieście, w którym grał już dwa razy. 
    Można śmiało powiedzieć, że jest popularny w wymienionych miastach, z których młodzi muzycy dojeżdżają do niego na naukę gry. 
    Trudno nie zauważyć jednak, że nie przekłada się to na popularność w Jego rodzinnych stronach. 
    Sławomir Czekalski nie ma uczniów harmonistów wśród młodzieży z okolic, dawno też Dobrzeliniacy nie grali u siebie na zabawie tanecznej.</p>

<p>W tym roku będziemy przełamywali złą passę muzyki ludowej w okolicach Żychlina. 
Dzięki dotacji MKiDN zapraszamy na warsztaty muzyczne wszystkie osoby posiadające podstawowe umiejętności gry na harmonii, akordeonie, skrzypcach, trąbce, czy innym akustycznym instrumencie melodycznym. 
Zapraszamy też bębnistów, bo to nie lada sztuka wybębnić charakterystycznego dla tych stron kujawiaka. A jeśli powstanie na koniec jedna albo dwie kapele? Tym lepiej dla muzyki, 
którą będziemy poznawali dzięki warsztatom ze Sławomirem Czekalskim.</p>

<p>Zapisy/pytania/informacje: Maria Stępień (koordynator projektu) tel. 502 150 322</p>
