<h2>Miasto w ruchu</h2>

<p>Celem projektu było wpłynięcie na wzrost partycypacji mieszkańców miast (w szczególności wojewódzkich) w zakresie przygotowania społecznych postulatów dotyczących uspokajania ruchu samochodowego w centrach oraz zebranie poparcia społecznego dla koniecznych zmian. Projekt ten realizowaliśmy od 1 kwietnia do 31 grudnia 2017 roku. Dzięki projektowi udało nam się, m.in.:

<ol>
    <li> Stworzyć raport „Odzyskajmy centra miast”; </li>
    <li> Wysłać 600 apeli obywateli z postulatami do decydentów; </li>
    <li> Wysłać raport „Odzyskajmy centra miast” wraz z apelem do Premiera Morawieckiego oraz 18 Prezydentów Miast Wojewódzkich i 16 Marszałków Województw; </li>
    <li> Zainicjować debatę z udziałem ok. 40 polityków, samorządowców, urzędników; </li>
    <li> Spopularyzować temat za pomocą imprezy towarzyszącej Muzyczna Podróż Tramwajem; </li>
</ol>

Więcej na temat projektu <a href="https://miastowruchu.pl/">miastowruchu.pl</a>
